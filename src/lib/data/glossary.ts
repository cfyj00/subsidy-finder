// 초성 추출 (한글 전용)
export function getInitialConsonant(str: string): string {
  const CONSONANTS = [
    'ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ',
    'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ',
  ];
  const code = str.charCodeAt(0) - 0xac00;
  if (code < 0 || code > 11171) return str[0];
  return CONSONANTS[Math.floor(code / 28 / 21)];
}

// 표시용 자음 (쌍자음은 기본자음으로 병합)
export function normalizeConsonant(c: string): string {
  const map: Record<string, string> = {
    ㄲ: 'ㄱ', ㄸ: 'ㄷ', ㅃ: 'ㅂ', ㅆ: 'ㅅ', ㅉ: 'ㅈ',
  };
  return map[c] ?? c;
}

