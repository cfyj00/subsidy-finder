/**
 * 검색어를 확장된 토큰 배열로 변환
 * "스마트팩토리" → ["스마트팩토리", "스마트 공장", "제조혁신", ...]
 */
export function expandSearchTokens(query: string): string[] {
  const normalized = query.trim().toLowerCase().replace(/\s+/g, '');
  const tokens: Set<string> = new Set();

  // 원본 (공백 포함/제거 둘 다)
  tokens.add(query.trim().toLowerCase());
  tokens.add(normalized);

  // 동의어 맵에서 확장
  for (const [key, synonyms] of Object.entries(KEYWORD_SYNONYMS)) {
    if (
      normalized.includes(key.toLowerCase()) ||
      key.toLowerCase().includes(normalized)
    ) {
      synonyms.forEach(s => {
        tokens.add(s.toLowerCase());
        tokens.add(s.toLowerCase().replace(/\s+/g, ''));
      });
    }
  }

  return Array.from(tokens);
}

/**
 * 프로그램 필드들을 하나의 검색 대상 문자열로 합칩니다.
 */
export function buildSearchTarget(p: {
  title: string;
  managing_org?: string | null;
  description?: string | null;
  category?: string;
  support_type?: string | null;
  target_industries?: string[];
  target_regions?: string[];
  eligibility_summary?: string | null;
}): string {
  return [
    p.title,
    p.managing_org ?? '',
    p.description ?? '',
    p.category ?? '',
    p.support_type ?? '',
    (p.target_industries ?? []).join(' '),
    (p.target_regions ?? []).join(' '),
    p.eligibility_summary ?? '',
  ]
    .join(' ')
    .toLowerCase();
}

/**
 * 프로그램이 검색어에 매칭되는지 판단
 */
export function matchesSearch(
  program: Parameters<typeof buildSearchTarget>[0],
  query: string,
): boolean {
  if (!query.trim()) return true;

  const target  = buildSearchTarget(program);
  const tokens  = expandSearchTokens(query);

  return tokens.some(token => target.includes(token));
}
